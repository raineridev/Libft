#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*mem;

	mem = malloc(nmemb * size);
	if (!mem)
		return (NULL);
	while (nmemb-- != 0)
	{
		((char *)mem)[nmemb] = '\0';
	}
	return (mem);
}
